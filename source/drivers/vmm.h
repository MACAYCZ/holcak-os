#include <stdint.h>

#define VMM_DIR_ENTRY_4KB_PRESENT         0x01
#define VMM_DIR_ENTRY_4KB_READ_ONLY       0x00
#define VMM_DIR_ENTRY_4KB_WRITABLE        0x02
#define VMM_DIR_ENTRY_4KB_USER            0x00
