#define VMM_DIR_ENTRY_4MB_ATTRIBUTE_TABLE 0x0800
#define VMM_DIR_ENTRY_4MB_ADDRESS(S) (((S) & 0xFFC00000) | ((S) >> 0x13 & 0x001FE000))

