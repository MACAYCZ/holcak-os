#define VMM_DIR_ENTRY_4KB_ACCESSED        0x20
#define VMM_DIR_ENTRY_4KB_ADDRESS(S) ((S) & 0xFFFFF000)

#define VMM_DIR_ENTRY_4MB_PRESENT         0x01
